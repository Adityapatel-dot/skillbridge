export function useTranslate(language) {
  const dict = translations[language] || translations.en
  return (key, params) => {
    let text = dict[key] || translations.en[key] || key
    if (params) {
      Object.entries(params).forEach(([k, v]) => {
        text = text.replace(`{${k}}`, v)
      })
    }
    return text
  }
}
